#include <unistd.h>

void ft_print_numbers(void)

{
	int i;
	char *str;
	i ='0';
	while(str[i]<='9')
	{
		write(1,&i,1);
		ft_putchar(str[i]);
		1++;
		{
			return(0);
		}
	}
}
